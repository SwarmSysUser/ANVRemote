/*****************************************************************************
 * Description: The hardware setup function
 *
 * Parameters:
 *
 *
 * Return value: 0 for success otherwise error code
 *
 *****************************************************************************/


void HardwareSetup()
{
                                    /* Initialise port 1 settings */
    IO.PCR1 = ANVR_SWITCH_PCR_SETTING;
    IO.PMR1.BYTE = ANVR_SWITCH_PMR_SETTING;
    IO.PUCR1.BYTE = ANVR_SWITCH_PUCR_SETTING;
                                    /* Initialise port 2 settings */
    IO.PCR2 = ANVR_SERIALPORT_PCR_SETTING;
                                    /* Initialise port 5 settings */
    IO.PCR5 = BIT_2 | BIT_3;
                                    /* Port 7 leave as initialised 
                                       by cpu reset microcode */
                                    /* Port 8 is used to control the LEDs */
                                    /* Initialise the data register so when
                                       the control register is loaded the 
                                       LEDs don't flash */
    IO.PDR8.BYTE = ANVR_LED_PCR_SETTING;
                                    /* Set led control pins as outputs */
    IO.PCR8 = ANVR_LED_PCR_SETTING;
                                    /* Stop all modules */
    MSTCR1.BYTE = (BYTE)(BIT_1 | BIT_2 | BIT_3 | BIT_4 | BIT_5);
    MSTCR2.BIT.MSTPWM = 1;

}

